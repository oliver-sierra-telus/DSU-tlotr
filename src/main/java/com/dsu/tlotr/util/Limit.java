package com.dsu.tlotr.util;

public class Limit {

    private final int MIN;
    private final int MAX;

    public Limit(int min, int max) {
        this.MIN = min;
        this.MAX = max;
    }

    public int getMIN() {
        return MIN;
    }

    public int getMAX() {
        return MAX;
    }
    

}
